// Google map
function initMap() {
    var jade_worli = {lat: 18.988435, lng: 72.815445};
    var map = new google.maps.Map(document.getElementById('map-canvas'), {
        zoom: 15,
        center: jade_worli,
        scrollwheel: false
    });

    var marker = new google.maps.Marker({
        position: jade_worli,
        map: map
    });
}

